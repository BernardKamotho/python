# Python Data types
# String - It is a series of characters that are inside of quotes
name = "Joseph Kainembuga"
print(name)
print(type(name))

# Numeric
# Int - This indicate a whole number in python. For an integer, we don't use quotes 
age = 39
print("The age of a person is: ", age)
print(type(age))

# float - This is a number with a decimal
coordinates = 15.4524
print("The coordinate are: ", coordinates)
print(type(coordinates))


